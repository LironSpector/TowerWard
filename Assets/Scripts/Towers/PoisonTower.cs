using UnityEngine;

/// <summary>
/// Description:
/// Represents a Poison Tower that applies poison effects to its targets.
/// This tower currently makes use of the base class functionality without custom logic.
/// </summary>
public class PoisonTower : ProjectileTower
{
    /// <summary>
    /// Called when the script instance is being loaded.
    /// Invokes the base Awake implementation.
    /// </summary>
    protected override void Awake()
    {
        base.Awake();
    }

    /// <summary>
    /// Called before the first frame update.
    /// Invokes the base Start implementation.
    /// </summary>
    protected override void Start()
    {
        base.Start();
    }
}
